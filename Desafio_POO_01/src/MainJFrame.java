    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        
        this.setAlwaysOnTop(false);
        
        try {
               if(csvFile.length()>0)
               {
                //adiciona nomes da cidade/regi√£o
                cidade = jTextField2.getText();
                   
                this.setAlwaysOnTop(false);

                Vector<Oferta> ofertas = new Vector<Oferta>();

                BufferedReader br = new BufferedReader(new FileReader(csvFile));

                while ((line = br.readLine()) != null) {
                    // separando os campos
                    String[] country = line.split(cvsSplitBy);
                    //System.out.println(line);

                    if (country[5].length() != 0) 
                    {
                       //System.out.println("+ "+country[5]);

                        boolean existe = false;

                        for (int i = 0; i < ofertas.size(); i++) {
                            if (ofertas.get(i).getNome().equals(country[5])) {
                                existe = true;
                            }
                        }

                        if (existe == false) {
                            Oferta o = new Oferta();
                            o.setNome(country[5]);
                            ofertas.add(o);
                        }

                        for (int i = 0; i < ofertas.size(); i++) {
                            if (ofertas.get(i).getNome().equals(country[5])) {
                                ofertas.get(i).setQuantidade(ofertas.get(i).getQuantidade() + 1);
                            }
                            //System.out.println(ofertas.get(i).getNome()+" : "+ofertas.get(i).getQuantidade());
                        }

                    }
                }

               //====================================================================

                DefaultPieDataset dataset = new DefaultPieDataset( );
                DefaultCategoryDataset dataset2 = new DefaultCategoryDataset( ); 
                DefaultPieDataset dataset3 = new DefaultPieDataset( );
          
                for(int i=0;i<ofertas.size();i++)
                {
                    System.out.println(ofertas.get(i).getNome()+" : "+ofertas.get(i).getQuantidade());
                    dataset.setValue( ofertas.get(i).getNome()+" : "+ofertas.get(i).getQuantidade(), ofertas.get(i).getQuantidade() ); 
                    dataset2.addValue( ofertas.get(i).getQuantidade() , ofertas.get(i).getNome(), cidade);  
                    dataset3.setValue( ofertas.get(i).getNome()+" : "+ofertas.get(i).getQuantidade(), ofertas.get(i).getQuantidade() ); 
                }
                
                int width = 1280;   /* Width of the image */
                int height = 720;  /* Height of the image */ 
                
                
                JFreeChart chart = ChartFactory.createPieChart(
                    "Ofertas em "+cidade,   // chart title
                    dataset,          // data
                    true,             // include legend
                    true,
                    false);
         
                File pieChart = new File( "PieChart.jpeg" ); 
                ChartUtilities.saveChartAsJPEG( pieChart , chart , width , height );
                
                //============================

                JFreeChart barChart = ChartFactory.createBarChart(
                    "Ofertas em "+cidade, 
                    "Bairro", "Ofertas", 
                    dataset2,PlotOrientation.VERTICAL, 
                    true, true, false);
         
                File BarChart = new File( "BarChart.jpeg" ); 
                ChartUtilities.saveChartAsJPEG( BarChart , barChart , width , height );
      
                //============================
        
                JFreeChart chart3 = ChartFactory.createPieChart3D( 
                    "Ofertas em "+cidade ,  // chart title                   
                    dataset3 ,         // data 
                    true ,            // include legend                   
                    true, 
                    false);

                PiePlot3D plot = ( PiePlot3D ) chart3.getPlot( );  
                plot.setStartAngle( 270 );                      
                plot.setInteriorGap( 0.02 );                                         
                File pieChart3D = new File( "pie_Chart3D.jpeg" );                           
                ChartUtilities.saveChartAsJPEG( pieChart3D , chart3 , width , height );
  
                
                //...
                 JOptionPane.showMessageDialog(this, "Graficos jerados com sucesso.", "Ate√ß√£o.", JOptionPane.WARNING_MESSAGE);
                
               }
               else
               {
                   JOptionPane.showMessageDialog(this, "Caregue antes o arquivo .csv.", "Ate√ß√£o.", JOptionPane.WARNING_MESSAGE);
               }
               
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Os graficos n√£o pureram ser gerados.", "Ate√ß√£o.", JOptionPane.WARNING_MESSAGE);
        }
        
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jToggleButton2ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        
          try
          {
        
            //Create a file chooser
            final JFileChooser fc = new JFileChooser();

            //In response to a button click:
            int returnVal = fc.showOpenDialog(this);
            
            if (returnVal == JFileChooser.APPROVE_OPTION) 
            {
                
            File file = fc.getSelectedFile();
            //This is where a real application would open the file.
            System.out.println("Opening: " + file.getName() );
            System.out.println(file.getCanonicalPath());
            
            csvFile = file.getCanonicalPath();
            
            jTextField1.setText(csvFile);
            
            this.setAlwaysOnTop(true);
        
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"O arquvo n√£o pode ser lido.","Ate√ß√£o.", JOptionPane.WARNING_MESSAGE);
        }
            
    }//GEN-LAST:event_jToggleButton1ActionPerformed

